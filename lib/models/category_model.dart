class Category {
  final String id;
  final String name;
  final String status;
  final int courses;
  final int students;
  final int revenue;
  final double rating;

  const Category({
    required this.id,
    required this.name,
    required this.status,
    required this.courses,
    required this.students,
    required this.revenue,
    required this.rating,
  });
}
